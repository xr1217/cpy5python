# Description: Converts distance in miles to kilometres

#Find distance in miles
mile = float(input("Enter distance in miles: "))

#Convert distance in miles to distance in kilometres
km = mile * 1.60934

#Display distance in kilometres
print ("Distance in kilometres: {0:.3f}".format(km))
